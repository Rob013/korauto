export const getFallbackOptionName = (code: string): string | undefined => {
  if (!code) {
    return undefined;
  }

  const normalized = code.trim();
  if (!normalized) {
    return undefined;
  }

  const candidateCodes = new Set<string>([
    normalized,
    normalized.padStart(3, "0"),
  ]);

  if (/^\d+$/.test(normalized)) {
    candidateCodes.add(Number.parseInt(normalized, 10).toString());
    if (normalized.length > 3) {
      const lastThree = normalized.slice(-3);
      candidateCodes.add(lastThree);
      candidateCodes.add(lastThree.padStart(3, "0"));
    }
  }

  for (const candidate of candidateCodes) {
    const fallback = KOREA_OPTION_FALLBACK_NAMES[candidate];
    if (fallback) {
      return fallback;
    }
  }

  return undefined;
};
